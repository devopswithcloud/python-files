# simple list array
my_list =[1,20,30,"two",3.0,True]

# accesssing by index
print(my_list[0])
print(my_list[1])
print(my_list[2])

# modify an element 
my_list[0] =10
print(my_list)

# adding or appending an element 
my_list.append(50) # adds at the end 
print(my_list)

my_list.insert(1,15) # insert 15 at the index 1
print(my_list)




#remove an element 
my_list.remove(30) # removes the first occurance of 30
print(my_list)



